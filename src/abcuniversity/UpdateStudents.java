    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
        Student student = new Student();
        
        student.setRegID(new Integer(txtRegID.getText()).intValue());
        student.setFirstName(txtFirstName.getText());
        student.setLastName(txtLastName.getText());
        student.setAge(Integer.parseInt(txtAge.getText().toString()));
        student.setAddress(txtAddress.getText());
        student.setGender(ddGender.getSelectedItem().toString());
        student.setFaculty(ddFaculty.getSelectedItem().toString());
        student.setDepartment(ddDepartment.getSelectedItem().toString());
        student.setYearOfReg(new Integer(ddYearOfRegistration.getSelectedItem().toString()).intValue());
        if(dbops.updateStudent(student)){
            JOptionPane.showMessageDialog(this, "Successfully Update the Records.!");
            this.dispose();
            return;
        }else{
            JOptionPane.showMessageDialog(this, "Error occured while updating.!");
            this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

